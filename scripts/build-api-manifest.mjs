#!/usr/bin/env node
// Generates api/_routes/_manifest.generated.js — the static import + route table
// that api/index.js mounts onto the Express app. Runs in prebuild (so Vercel
// always ships a fresh manifest) and is committed (so lint/build/smoke work
// without a generate step). scripts/verify-api-manifest.mjs fails if it drifts.
//
// Output is deterministic (sorted): static routes before parameterized ones so a
// `:param` route never shadows a static sibling.
//
//   node scripts/build-api-manifest.mjs            # write
//   node scripts/build-api-manifest.mjs --check    # exit 1 if on-disk differs

import { readdirSync, statSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, resolve, relative, sep } from 'node:path'
import { fileURLToPath } from 'node:url'
import { routePathFor } from './api-consolidation.config.mjs'

const ROOT = resolve(fileURLToPath(import.meta.url), '..', '..')
const ROUTES = join(ROOT, 'api', '_routes')
const OUT = join(ROUTES, '_manifest.generated.js')
const CHECK = process.argv.includes('--check')

const toPosix = (p) => p.split(sep).join('/')

function walk(dir, acc = []) {
  if (!existsSync(dir)) return acc
  for (const name of readdirSync(dir)) {
    const full = join(dir, name)
    if (statSync(full).isDirectory()) walk(full, acc)
    else if (name.endsWith('.js') && full !== OUT) acc.push(full)
  }
  return acc
}

const files = walk(ROUTES).map((abs) => {
  const rel = toPosix(relative(ROUTES, abs))
  return { rel, route: routePathFor(rel) }
})

// Static routes (no ':') before parameterized; then deeper paths first; then alpha.
files.sort((a, b) => {
  const pa = (a.route.match(/:/g) || []).length
  const pb = (b.route.match(/:/g) || []).length
  if (pa !== pb) return pa - pb
  const da = a.route.split('/').length
  const db = b.route.split('/').length
  if (da !== db) return db - da
  return a.route.localeCompare(b.route)
})

const imports = files.map((f, i) => `import h${i} from './${f.rel}'`).join('\n')
const table = files.map((f, i) => `  { path: ${JSON.stringify(f.route)}, handler: h${i} },`).join('\n')

const body = `/* eslint-disable */
// AUTO-GENERATED by scripts/build-api-manifest.mjs — DO NOT EDIT BY HAND.
// Regenerated in prebuild; scripts/verify-api-manifest.mjs fails if this drifts.
// ${files.length} consolidated routes.
${imports}

export const routes = [
${table}
]
`

if (CHECK) {
  const current = existsSync(OUT) ? readFileSync(OUT, 'utf8') : ''
  if (current !== body) {
    console.error('✗ api manifest is stale — run: node scripts/build-api-manifest.mjs')
    process.exit(1)
  }
  console.log(`✓ api manifest in sync (${files.length} routes)`)
} else {
  writeFileSync(OUT, body)
  console.log(`✓ wrote ${toPosix(relative(ROOT, OUT))} (${files.length} routes)`)
}
